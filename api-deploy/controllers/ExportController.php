<?php

namespace App\Controllers;

use PDO;

class ExportController
{
    private PDO $db;

    public function __construct()
    {
        $this->db = \App\Config\Database::getConnection();
    }

    public function export(): void
    {
        $userId = \App\Middleware\AuthMiddleware::getUserId();
        $input = $this->getJsonInput();

        $format = $input['format'] ?? 'pdf';
        $analysisId = $input['analysis_id'] ?? null;

        if (!$analysisId) {
            Response::error('analysis_id is required', 400);
            return;
        }

        // Get analysis
        $sql = "SELECT * FROM cv_analyses WHERE id = :id AND user_id = :user_id LIMIT 1";
        $stmt = $this->db->prepare($sql);
        $stmt->execute([':id' => $analysisId, ':user_id' => $userId]);
        $analysis = $stmt->fetch();

        if (!$analysis) {
            Response::error('Analysis not found', 404);
            return;
        }

        $analysis['analysis_data'] = json_decode($analysis['analysis_data'], true);

        switch ($format) {
            case 'json':
                $this->exportJSON($analysis);
                break;
            case 'html':
                $this->exportHTML($analysis);
                break;
            case 'docx':
                $this->exportDOCX($analysis);
                break;
            default:
                Response::error('Unsupported export format. Use: json, html, docx', 400);
        }
    }

    private function exportJSON(array $analysis): void
    {
        header('Content-Type: application/json');
        header('Content-Disposition: attachment; filename="cv-analysis-' . $analysis['id'] . '.json"');

        echo json_encode($analysis['analysis_data'], JSON_PRETTY_PRINT);
        exit;
    }

    private function exportHTML(array $analysis): void
    {
        $data = $analysis['analysis_data'];

        header('Content-Type: text/html');
        header('Content-Disposition: attachment; filename="cv-analysis-' . $analysis['id'] . '.html"');

        $html = $this->renderHTML($data);

        echo $html;
        exit;
    }

