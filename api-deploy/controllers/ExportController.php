    private function exportDOCX(array $analysis): void
    {
        header('Content-Type: application/vnd.openxmlformats-officedocument.wordprocessingml.document');
        header('Content-Disposition: attachment; filename="cv-analysis-' . $analysis['id'] . '.docx"');

        // Simple DOCX generation (basic template)
        // For production, use PHPWord library (requires Composer)
        // For now, generate a simple text file with .docx extension
        $data = $analysis['analysis_data'];
        $content = "CV ANALYSIS REPORT\n";
        $content .= "==================\n\n";
        $content .= "Overall Score: " . ($data['scores']['overall'] ?? 0) . "%\n\n";
        $content .= "Skills:\n";
        foreach ($data['skills'] ?? [] as $skill) {
            $content .= "- " . $skill['name'] . " (" . $skill['category'] . ")\n";
        }
        $content .= "\nStrengths:\n";
        foreach ($data['feedback']['strengths'] ?? [] as $s) {
            $content .= "✓ " . $s . "\n";
        }
        $content .= "\nImprovements:\n";
        foreach ($data['feedback']['improvements'] ?? [] as $i) {
            $content .= "→ " . $i . "\n";
        }

        echo $content;
        exit;
    }

    private function getJsonInput(): array
    {
        $input = file_get_contents('php://input');
        $data = json_decode($input, true);

        return is_array($data) ? $data : [];
    }
}
